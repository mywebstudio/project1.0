        <p>Кухни Скаволини с 1984 года считаются в Европе символом качества, респектабельности и хорошего вкуса. Каталог
            Скаволини — универсальная подборка практичных интерьерных решений: четыре десятка модульных коллекций,
            состоящих из множества функциональных элементов, используются для создания стильных индивидуальных проектов.
            Наш интернет-магазин (официальный дилер производителя в Москве) предлагает: </p>

        <ul>
            <li>большой выбор классических и ультрасовременных кухонных гарнитуров из натуральных, экологически
                безопасных материалов (массива дуба, ореха, каштана, вишни, ламинированных плит МДФ) в широком цветовом
                диапазоне — ознакомившись с фотографиями и описаниями на сайте, вы сможете подобрать оптимальный размер,
                конфигурацию и оттенок кухни;
            </li>

            <li>фабричную гарантию на купленные товары;</li>

            <li>бесплатные консультации профессиональных дизайнеров — поможем рассчитать габариты и стоимость вашей
                новой итальянской кухни до оформления заказа;
            </li>

            <li>полный спектр услуг, связанных с адресной доставкой, сборкой и монтажом продукции Scavolini.</li>
        </ul>

        <p></p>

        <p>Работая в двух наиболее популярных направлениях (классика и модерн), дизайнеры Scavolini создают добротную
            кухонную мебель, задающую тон всему интерьеру. Лаконичная красота столешниц из гранита, мрамора и других
            видов природного или искусственного камня ярко контрастирует с изысканностью резных и лакированных
            деревянных фасадов.</p>

        <p>Износоустойчивая стальная и латунная фурнитура в сочетании с множеством подвесных, выдвижных и встроенных
            аксессуаров служит залогом комфортной и продолжительной эксплуатации мебели. Палитра, используемая
            производителем, не ограничивается традиционным набором оттенков: эстеты по достоинству оценят эффектные
            гарнитуры в бирюзовом, вишневом, стальном, золотом или темно-синем цвете.</p>
